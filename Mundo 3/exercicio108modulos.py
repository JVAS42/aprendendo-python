def calcularMetade(valor):
    metade = valor/2
    print(f'A metade R${valor:,.2f} é R${metade:,.2f}'.replace(".", ","))


def calcularDobro(valor):
    dobro = valor*2
    print(f'A metade R${valor:,.2f} é R${dobro:,.2f}'.replace(".", ","))


def calcularAcressimo(valor):
    acressimo = valor*1.10
    print(f'A metade R${valor:,.2f} é R${acressimo:,.2f}'.replace(".", ","))